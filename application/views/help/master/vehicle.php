<h3>Vehicles</h3>
<p>Vehicle Untuk Membuat Jenis Data Kendaraan Internal</p>
<p>1. Pilih Menu <b>Master</b> - <b>Vehicles</b> Lalu Pilih Tombol <b>+Create Vehicle</b></p>
<img src="<?= base_url('uploads/help/master/vehicle/1.png') ?>" width="800" height="480"/>
<p>2. Isi Form </p>
<p><b>Vehicle Name :</b> Isi Nama Kendaraan</p>
<p><b>Vehicle Type :</b> Isi Jenis Kendaraan</p>
<p><b>Plate Number :</b> Isi Dengan Nomer Plat Kendaraan</p>
<p><b>Description :</b> Isi Sesuai Description</p>
<p>Bila Sudah Isi Form, Pilih Tombol <b>Save Vehicle</b></p>
<img src="<?= base_url('uploads/help/master/vehicle/2.png') ?>" width="800" height="480"/>
