<h3>People</h3>
<P>People Untuk Membuat Data Data, Seperti Customer, TPS, Shippingline Dll</P>
<p>1. Pilih Menu <b>Master</b> - <b>People</b> Pilih Tombol <b>+Create Person</b> </p>
<img src="<?= base_url('uploads/help/master/people/1.png') ?>" width="800" height="480"/>
<p>2. Isi Form</p>
<p><b>Type :</b> Pilih Sesuai Type Yang Ingin Dibuat</p>
<p><b>No Person :</b> Isi Sesuai No Code yang dibuat</p>
<p><b>Name :</b> Isi Sesuai Nama</p>
<p><b>Address :</b> Isi Sesuai Alamat</p>
<p><b>Gender : </b> Pilih Sesuai Gender</p>
<p><b>Birthday :</b> Sesuai Tanggal Lahir</p>
<p><b>Contact :</b> Isi Sesuai Contact</p>
<p><b>Email :</b> Isi Sesuai Email</p>
<p><b>Branch :</b> Pilih Sesuai Branch</p>
<p><b>Handling Type :</b> Pilih Sesuai Handling</p>
<p>Bila Sudah Diisi Form Pilih <b>Save Person</b></p>
