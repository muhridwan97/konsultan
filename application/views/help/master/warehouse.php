<h3>Warehouses</h3>
<p><b>Warehouse : </b> Untuk Pembuatan Warehouse berhubungan dengan dengan Branch, buat <a href="<?= site_url('help?page=master&section=branch') ?>" style="text-decoration:none">Branch</a> terlebih dahulu</p>
<p>1. Pilih Menu <b>Master</b> - <b>Warehouses</b> Lalu Pilih Tombol <b>+Create Warehouse</b></p>
<img src="<?= base_url('uploads/help/master/warehouse/1.png') ?>" width="800" height="480"/>
<p>2. Isi Form</p>
<p><b>Branch :</b> Pilih Branch yang sudah dibuat</p>
<p><b>Warehouse Name :</b> Isi Sesuai Nama Warehouse</p>
<p><b>Warehouse Description :</b> Isi Sesuai Description</p>
<p>Bila Sudah Disi, Pilih Tombol <b>Save Warehouse</b></p>
<img src="<?= base_url('uploads/help/master/warehouse/2.png') ?>" width="800" height="480"/>
<p>3. Warehouse Berhasil Dibuat </p>
