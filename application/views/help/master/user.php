<h3>User</h3>
<P>User : untuk membuat user login pada system </P>
<p>1. Pilih Menu <b>Master</b> - <b>User</b> Lalu Pilih <b>Create User</b></p>
<img src="<?= base_url('uploads/help/master/user/1.png') ?>" width="800" height="480"/>
<p>2. Isi Form</p>
<p><b>Name : </b> Isi Sesuai Nama</p>
<p><b>Username : </b> Isi Sesuai Username</p>
<p><b>Email : </b> Isi Sesuai Email</p>
<p><b>User Type :</b> Pilih Sesuai Type</p>
<ul>
    <li><b>Internal :</b> Untuk User TCI</li>
    <li><b>External :</b> Untuk Customer</li>
</ul>
<p><b>Password & Confirm Password : </b> Isi Sesuai Password</p>
<p><b>Status :</b> Pilih Sesuai Status</p>
<ul>
    <li><b>Pending :</b> User Terpending</li>
    <li><b>Actived :</b> User Diaktifkan</li>
    <li><b>Suspended :</b> User Tersuspended</li>
</ul>
<p><b>Profile :</b> Pilih yang sudah di buat di menu <a href="people.html" style="text-decoration:none">People</a></p>
<p><b>Roles : </b>Pilih yang sudah dibuat di menu <a href="roles.html" style="text-decoration:none">Roles</a></p>
<p>Bila Sudah Diisi Form, Pilih <b>Save User</b></p>
