<h3>Safe Conduct</h3>
<p>1. Pilih Menu <b> Delivery Order</b> lalu pilih <b>Safe Conduct</b></p>
<p>2. Pilih Tombol <b>+Create Safe Conduct</b></p>
<img src="<?= base_url('uploads/help/inbound/safe-conduct/1.png') ?>" width="800" height="480"/>
<p>3. Isi Form Safe Conduct</p>
<p><b>Category : </b> Pilih Inbound</p>
<p><b>Warehouse Of Origin : </b> Pilih Tujuan Gudang</p>
<p><b>Expedition Type : </b> Pilih <b>Internal</b></p>
<p><b>Vehicle :</b> Pilih Jenis Kendaraan</p>
<p><b>Driver :</b> Pilih Nama Supir</p>
<p><b>E-Seal: </b> Kosongkan</p>
<p><b>Attachment : </b> Kosongkan</p>
<p><b>Descriptionn :</b> Isi Sesuai Description</p>
<img src="<?= base_url('uploads/help/inbound/safe-conduct/2.png') ?>" width="800" height="480"/>
<p><b>Booking : </b> Pilih Nomer Booking</p>
<p><b>Containers :</b> Pilih Tombol <b>Take</b>, Untuk memilih Container</p>
<p>Note : Bila di combo bisa take dua Container untuk ukuran 20 Feet (Untuk Satu Job Surat Jalan)</p>
<p>Bila Sudah Sesuai, Pilih Tombol <b>Save Safe Conduct</b></p>
<img src="<?= base_url('uploads/help/inbound/safe-conduct/3.png') ?>" width="800" height="480"/>
<p>Safe Conduct Berhasil Dilakukan</p>
<img src="<?= base_url('uploads/help/inbound/safe-conduct/4.png') ?>" width="800" height="480"/>
<p>Untuk Print Surat Jalan, Pilih Tombol <b>Action</b> lalu Pilih <b>Print</b></p>
<img src="<?= base_url('uploads/help/inbound/safe-conduct/5.png') ?>" width="800" height="480"/>
<p>Pilih Tombol <b>Print Now</b></p>
<img src="<?= base_url('uploads/help/inbound/safe-conduct/6.png') ?>" width="800" height="480"/>
<p>Contoh Surat Jalan (Inbound), Print Surat Jala Bisa Diberikan Ke Security Untuk Discan Nomer Surat Jalan</p>