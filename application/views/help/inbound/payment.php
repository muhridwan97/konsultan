<h3>Request Payment</h3>
<p>1. Pilih Menu <b>Payment</b> - <b>Booking Payments</b></p>
<p>2. Pilih Tombol <b>+Create Payment</b></p>
<img src="<?= base_url('uploads/help/inbound/payment/0.png') ?>" width="800" height="480"/>
<p>3. Isi Form Seperti Dibawah Ini :</p>
<p><b>Booking :</b> Pilih Nomer Booking</p>
<p><b>Payment Category :</b>Pilih <b>Billing</b></p>
<p><b>Payment Type : </b>Pilih <b>Obtps</b></p>
<p>Bila sudah sesuai Pilih Tombol <b>Save Payment</b></p>
<img src="<?= base_url('uploads/help/inbound/payment/1.png') ?>" width="800" height="480"/>
<p>Payment berhasil dibuat</p>
<img src="<?= base_url('uploads/help/inbound/payment/2.png') ?>" width="800" height="480"/>
<p>4. Untuk melakukan print, Pilih Tombol <b>Action</b> lalu Pilih <b>Print Payment</b></p>
<img src="<?= base_url('uploads/help/inbound/payment/3.png') ?>" width="800" height="480"/>
<p>5. Hasil Print, Tanda Tangan dan diberikan kepada Finance</p>
<img src="<?= base_url('uploads/help/inbound/payment/4.png') ?>" width="800"/>
<p></p>


<h3>Payment Validation</h3>
<p>1. Pilih Menu Payment lalu pilih Booking Payments<p>
<p>2. Pilih Data yang sudah di booking oleh operasional</p>
<p>3. Pilih Tombol Action, Lalu Pilih Validate</p>
<img src="<?= base_url('uploads/help/inbound/payment/validate/1.png') ?>" width="800" height="480"/>
<p>4. Pilih <b>Approve</b></p>
<img src="<?= base_url('uploads/help/inbound/payment/validate/2.png') ?>" width="800" height="480"/>
<p>5. Validate Berhasil Dilakukan, Status dari Draft berubah Menjadi Validate</p>
<img src="<?= base_url('uploads/help/inbound/payment/validate/3.png') ?>" width="800" height="480"/>

<h3>Payment Realization</h3>
<p>1. Pilih Menu Payment lalu pilih Booking Payments<p>
<p>2. Pilih Data yang sudah di validate</p>
<p>3. Pilih Tombol Action, Lalu Pilih  Realization</p>
<img src="<?= base_url('uploads/help/inbound/payment/realization/1.png') ?>" width="800" height="480"/>
<p>4. Isi Data Sesuai Realisasi </p>
<p><b>Amount Realization : </b> Isi sesuai kegiatan</p>
<p><b>Payment Date : </b> Pilih sesuai tanggal</p>
<p><b>No. Invoice : </b> Masukan Nomer invoice obtps</p>
<p><b>Realization Attachment : </b> Upload File Dokumen</p>
<p>Bila sudah disi semua, Pilih Tombol <b>Update Payment</b></p>
<img src="<?= base_url('uploads/help/inbound/payment/realization/2.png') ?>" width="800" height="480"/>
<p>5. Realized Berhasil Dilakukan</p>
<img src="<?= base_url('uploads/help/inbound/payment/realization/3.png') ?>" width="800" height="480"/>