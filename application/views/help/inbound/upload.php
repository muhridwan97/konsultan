<h3>Upload Document</h3>
<h4>BCF 1.5 (Inbound)</h4>
<p>Pilih Menu <b>Upload</b> Lalu Pilih <b>Document</b></p>
<p>1. Pilih Tombol <b>+Upload Documents</b></p>
<img src="<?= base_url('uploads/help/inbound/upload/1.png') ?>" width="800" height="480"/>
<br>
<p>2.<b> Customer/Supplier : </b> Pilih Sesuai Customer</p>
<p>3.<b> Upload Title : </b> Isi dengan Format <b>BCF 1.5 + (Nomer BCF 1.5)</b></p>
<p>4.<b> Upload Type : </b> Pilih <b>BCF 1.5 (Inbound)</b></p>
<p>5.<b> Document Number : </b> Isi sesuai dokumen</p>
<p>6.<b> Document Date :</b> Pilih sesuai tanggal pada dokumen</p>
<p>7. Pilih tombol <b>Select File</b> lalu pilih dokumen</p>
<p>8. Pilih tombol <b>Save Upload</b></p>
<img src="<?= base_url('uploads/help/inbound/upload/3.png') ?>" width="800" height="480"/>
<p>9. Lihat <b>Status Valid</b> upload dan Docs pada gambar <b>Status Valid</b> masih (NO), cara melihat untuk berubah</p>
<img src="<?= base_url('uploads/help/inbound/upload/4.png') ?>" width="800" height="480"/>
<p>10. Pilih Tombol <b>Action</b> lalu pilih <b>View Documents (Untuk Melihat File Yang sudah Diupload)</b></p>
<img src="<?= base_url('uploads/help/inbound/upload/4.png') ?>" width="800" height="480"/>
<p>11. Pilih Tombol <b>Action</b> lalu pilih  View Files</p>
<p>12. Pilih Tombol <b>Download</b> untuk melihat dokumen yang sudah diupload</p>
<p>13. Bila sudah melihat dokumen yang telah diupload, kembali lagi kemenu <b>Upload Document</b>, untuk validate document</p>
<img src="<?= base_url('uploads/help/inbound/upload/5.png') ?>" width="800" height="480"/>
<p>14. Pilih Tombol <b>Action</b> Lalu Pilih <b>Validate Document</b></p>
<img src="<?= base_url('uploads/help/inbound/upload/5.png') ?>" width="800" height="480"/>
<p>15. Pilih <b>Approve</b></p>
<p> Note: bila dokumen sesuai pilih tombol <b>Approve</b>, bila dokumen tidak sesuai pilih tombol <b>Reject</b></p>
<img src="<?= base_url('uploads/help/inbound/upload/6.png') ?>" width="800" height="480"/>
<p>16. Document berhasil di approve </p>
<p>17. Pilih Tombol <b>Back To Upload List</b></p>
<img src="<?= base_url('uploads/help/inbound/upload/7.png') ?>" width="800" height="480"/>
<p>18. Pilih Tombol <b>Action</b> Pilih Validate Upload</p>
<img src="<?= base_url('uploads/help/inbound/upload/8.png') ?>" width="800" height="480"/>
<p>19. Pilih Tombol <b>Approve</b></p>
<img src="<?= base_url('uploads/help/inbound/upload/9.png') ?>" width="800" height="480"/>
<p>20. Prosses Upload Berahasil Dilakukan</p>
<img src="<?= base_url('uploads/help/inbound/upload/10.png') ?>" width="800" height="480"/>