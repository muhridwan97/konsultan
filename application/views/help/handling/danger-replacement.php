<h3>Danger Replacement</h3>
<p>1. Pilih Menu <b>Handling</b> Lalu Pilih <b>Danger Replacement</b>, Lalu Pilih Tombol <b>+Replace Danger Status</b></p>
<img src="<?= base_url('uploads/help/handling/danger-replacement/1.png') ?>" width="800" height="480"/>
<p>2. Isi Form</p>
<p><b>Booking :</b> Pilih Data Booking</p>
<p>Pada <b>Stock Containers</b> Take Container Yang Ingin Diganti Statusnya</p>
<p><b>Change Danger To :</b> Sesuaikan Dengan Bagian Finnace</p>
<p>Bila Sudah Diisi Semua Pilih Tombol <b>Save Danger Replacement</b></p>
<img src="<?= base_url('uploads/help/handling/danger-replacement/2.png') ?>" width="800" height="480"/>
<p>3. Danger Replacement Berhasil Dilakukan</p>
<img src="<?= base_url('uploads/help/handling/danger-replacement/3.png') ?>" width="800" height="480"/>
<p>4. Pilih Tombol <b>Action</b> Lalu Pilih <b>Validate</b></p>
<img src="<?= base_url('uploads/help/handling/danger-replacement/4.png') ?>" width="800" height="480"/>
<p>5. Pilih Tombol <b>Approve And Commit New Status</b></p>
<img src="<?= base_url('uploads/help/handling/danger-replacement/5.png') ?>" width="800" height="480"/>
<p>6. Validate Berhasil Dilakukan</p>
<img src="<?= base_url('uploads/help/handling/danger-replacement/6.png') ?>" width="800" height="480"/>